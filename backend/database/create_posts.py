# 发布文章
def create_posts():
    print("开始发布文章到博客系统...")
    
    for i, article in enumerate(articles, 1):
        try:
            response = requests.post(f"{BASE_URL}/posts", json=article)
            if response.status_code == 200:
                print(f"✅ 第{i}篇文章发布成功: {article['title']}")
            else:
                print(f"❌ 第{i}篇文章发布失败: {response.status_code} - {response.text}")
        except Exception as e:
            print(f"❌ 第{i}篇文章发布异常: {str(e)}")
    
    print("\n文章发布完成！")

if __name__ == "__main__":
    create_posts()